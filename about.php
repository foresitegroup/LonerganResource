<strong>Lonergan Resource</strong> is a second website devoted to Lonergan Studies sponsored by the Marquette University Lonergan Project. It joins <a href="http://www.bernardlonergan.com"><strong>Lonergan Archive</strong></a>, where primary materials from the Lonergan archival papers and tapes are stored.<br>
<br>
<strong>Lonergan Resource</strong> is a repository of secondary-source materials that could prove to be valuable for Lonergan Studies. It will include major papers and articles on Lonergan, recordings of major lectures and conferences, and longer monographs that are either republished from books or composed for this website itself. In every instance, permission has been obtained from the original author or speaker, and notice of copyright has been indicated. The persons primarily responsible for the site are Robert M. Doran, S.J., and Greg Lauzon.
<br>
<br>
<strong>Collaborators include</strong> the <a href="http://www.lonergan-lri.ca">Lonergan Research Institute, Toronto</a>, the <a href="http://www.bc.edu/bc_org/avp/cas/lonergan/default.html">Boston College Lonergan Institute</a>, Lonergan Workshop, and the <a href="http://www2.lmu.edu/lonergan">Los Angeles Lonergan Center</a>. John Dadosky of Regis College, Toronto, and Mark Morelli of Loyola Marymount University assist the organizers in a consultative fashion.

